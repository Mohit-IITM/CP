#include <iostream>
using namespace std;
void solve()
{
    int a[3][3], b[3][3];
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            cin >> a[i][j];
            b[i][j] = 1;
        }
    }
    for (int i = 0; i < 3; i++)
    {
        b[0][0] += a[0][0] + a[1][0] + a[0][1];
        b[0][1] += a[0][1] + a[1][1] + a[0][0] + a[0][2];
        b[0][2] += a[0][2] + a[1][2] + a[0][1];
        b[1][0] += a[1][0] + a[0][0] + a[1][1] + a[2][0];
        b[1][1] += a[1][1] + a[0][1] + a[1][0] + a[1][2] + a[2][1];
        b[1][2] += a[1][2] + a[0][2] + a[1][1] + a[2][2];
        b[2][0] += a[2][0] + a[1][0] + a[2][1];
        b[2][1] += a[2][1] + a[1][1] + a[2][0] + a[2][2];
        b[2][2] += a[2][2] + a[1][2] + a[2][1];
    }
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            cout << b[i][j] % 2;
        }
        cout << endl;
    }
}
int main()
{
    solve();
}